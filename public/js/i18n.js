    /**
     * Initialize i18n system
     */
    init() {
        // Load saved language preference
        const savedLang = localStorage.getItem('app_language');
        if (savedLang && this.translations[savedLang]) {
            this.currentLang = savedLang;
            console.log('📖 Loaded language from localStorage:', savedLang);
        } else {
            console.log('🆕 Using default language:', this.currentLang);
        }
        
        // Apply translations to the page
        this.applyTranslations();
        
        // Update language switcher UI
        this.updateLanguageSwitcher();
        
        // Setup language switcher event listeners
        this.setupLanguageSwitcher();
        
        console.log('🌐 i18n initialized with language:', this.currentLang);
    },
    
    /**
     * Setup language switcher event listeners
     */
    setupLanguageSwitcher() {
        const languageSwitcher = document.getElementById('languageSwitcher');
        if (!languageSwitcher) {
            console.warn('⚠️ Language switcher not found in DOM');
            return;
        }
        
        const langButtons = languageSwitcher.querySelectorAll('.lang-btn');
        langButtons.forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = btn.getAttribute('data-lang');
                if (lang) {
                    console.log('🔄 Switching to language:', lang);
                    this.setLanguage(lang);
                }
            });
        });
        
        console.log('✅ Language switcher event listeners attached');
    },
    
    /**
     * Get translation for a key
     */
    t(key) {
        const translation = this.translations[this.currentLang][key];
        if (!translation) {
            console.warn(`Translation missing for key: ${key} in language: ${this.currentLang}`);
            return key;
        }
        return translation;
    },
    
    /**
     * Change current language
     */
    setLanguage(lang) {
        if (!this.translations[lang]) {
            console.error(`Language not supported: ${lang}`);
            return;
        }
        
        this.currentLang = lang;
        localStorage.setItem('app_language', lang);
        
        // Apply translations
        this.applyTranslations();
        
        // Update switcher UI
        this.updateLanguageSwitcher();
        
        // Trigger custom event for components to update
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
        
        console.log('🌐 Language changed to:', lang);
    },
    
    /**
     * Apply translations to all elements with data-i18n attribute
     */
    applyTranslations() {
        // Translate elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            
            // Check if element has data-i18n-attr for attribute translation
            const attr = element.getAttribute('data-i18n-attr');
            if (attr) {
                element.setAttribute(attr, translation);
            } else {
                element.textContent = translation;
            }
        });
        
        // Translate placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            element.placeholder = this.t(key);
        });
        
        // Translate titles
        document.querySelectorAll('[data-i18n-title]').forEach(element => {
            const key = element.getAttribute('data-i18n-title');
            element.title = this.t(key);
        });
        
        // Update page title
        const titleElement = document.querySelector('title');
        if (titleElement && titleElement.hasAttribute('data-i18n')) {
            const key = titleElement.getAttribute('data-i18n');
            titleElement.textContent = this.t(key);
        }
    },
    
    /**
     * Update language switcher UI
     */
    updateLanguageSwitcher() {
        const switcher = document.getElementById('languageSwitcher');
        if (!switcher) return;
        
        const buttons = switcher.querySelectorAll('.lang-btn');
        buttons.forEach(btn => {
            const lang = btn.getAttribute('data-lang');
            if (lang === this.currentLang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    },
    
    /**
     * Get current language
     */
    getCurrentLanguage() {
        return this.currentLang;
    },
    
    /**
     * Get language name
     */
    getLanguageName(lang) {
        const names = {
            'en': 'English',
            'id': 'Indonesia',
            'zh-TW': '繁體中文'
        };
        return names[lang] || lang;
    }
